package casting;

public class PrimitiveCassting {
	
	public static void main(String[] args) {
//		Implicit casting
		
		byte b = 20;
		
		int c = (int)b;
		
		System.out.println(c);//20
		
		
		short s = 90;
		
		long l = (long)s;
		
		System.out.println(l);//90
		
		
//		Explicit casting
		
		
		int i = 129;
		
	byte t	= (byte)i;
	
	System.out.println(t);
		
		
		
	}
	
	
		

}
